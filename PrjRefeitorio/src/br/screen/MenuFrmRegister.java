    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        if (tfDesc.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe a Descrição!");
            tfDesc.requestFocus();
            return;
        }
        if (tfDate.getText().equals("  /  /    ")) {
            JOptionPane.showMessageDialog(rootPane, "Informe da data!");
            tfDate.requestFocus();
            return;
        }
        if (cbMeal.getSelectedIndex()==0) {
            JOptionPane.showMessageDialog(rootPane, "Informe a refeição!");
            cbMeal.requestFocus();
            return;
        }
        
        String date = tfDate.getText();
        try {
            DateFormat fmt = new SimpleDateFormat("dd/MM/yyyy");
            java.util.Date data;
            data = new java.util.Date(fmt.parse(date).getTime());
            menu.setDate(data);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Formata da dáta é inválido!", "IFCE", 
                    JOptionPane.ERROR_MESSAGE);
        }

        menu.setDescription(tfDesc.getText());
        menu.setCampus(UserActive.returnCampus());
        menu.setMeal((Meal) cbMeal.getSelectedItem());

        MenuDAO mDAO = new MenuDAO();
        menu.setCampus(UserActive.returnCampus());
        if (menu.getId() == null) {
            if(mDAO.checkExists("date", menu.getDate(), "meal", menu.getMeal()).size()>0){
                JOptionPane.showMessageDialog(rootPane, "Já existe um cardápio para essa refeição", "IFCE", 
                    JOptionPane.ERROR_MESSAGE);
                return;
            }
            mDAO.add(menu);
        } else {
            mDAO.update(menu);
        }
        setVisible(false);

    }//GEN-LAST:event_jButton2ActionPerformed

