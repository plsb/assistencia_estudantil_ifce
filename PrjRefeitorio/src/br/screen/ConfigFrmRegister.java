    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        if (tfVersionSystem.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe a Versão do Sistema!");
            tfVersionSystem.requestFocus();
            return;
        }
        
        try {
            config.setVersion(Double.parseDouble(tfVersionSystem.getText()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, 
                    "Versão inválida.", "IFCE", JOptionPane.ERROR_MESSAGE);
            return ;
        }
      
        if (config.getId() == null) {
            dao.add(config);
        } else {
            dao.update(config);
        }
        setVisible(false);

    }//GEN-LAST:event_jButton2ActionPerformed

