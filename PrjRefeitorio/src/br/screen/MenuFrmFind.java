    public void preencheTabela(String texto) {
        MenuDAO mDAO = new MenuDAO();
        if (texto.equals("  /  /    ")) {
            MenuTableModel ptm = new MenuTableModel(mDAO.checkExists("campus", UserActive.returnCampus()));
            tbMenu.setModel(ptm);
        } else {
            String date = texto;
            try {
                DateFormat fmt = new SimpleDateFormat("dd/MM/yyyy");
                java.util.Date data;
                data = new java.util.Date(fmt.parse(date).getTime());
                MenuTableModel ptm = new MenuTableModel(mDAO.checkExists("date", data,
                                    "campus", UserActive.returnCampus()));
                tbMenu.setModel(ptm);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane, "Data inválida!", "IFCE",
                        JOptionPane.ERROR_MESSAGE);
            }
            
        }

        FormatSizeColJTable.packColumns(tbMenu, 1);
    }

