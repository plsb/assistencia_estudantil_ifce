    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (tbMenu.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha da tabela!");
        } else {
            MenuTableModel ptm = (MenuTableModel) tbMenu.getModel();
            Menu select = ptm.getMenu(tbMenu.getSelectedRow());
            if (JOptionPane.showConfirmDialog(rootPane, "Deseja Excluir o Cardápio " + select.getDescription() + "?",
                    "Excluir Cardápio", JOptionPane.YES_NO_OPTION)
                    == JOptionPane.YES_OPTION) {
                MenuDAO mDAO = new MenuDAO();
                try {
                    mDAO.remove(select);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(rootPane, "Cardápio não pode ser excluído!");
                }
                preencheTabela(tfSearchDate.getText().toString());
            }

        }
    }//GEN-LAST:event_btnDeleteActionPerformed

