    private void btSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSaveActionPerformed
        // TODO add your handling code here:
        UserDAO uDAO = new UserDAO();
        User user = UserActive.retornaUsuarioAtivo();
        if (tfPassBefore.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe a senha anterior!",
                    "IFCE", JOptionPane.ERROR_MESSAGE);
            tfPassBefore.requestFocus();
            return;
        }
        if(!Util.md5(tfPassBefore.getText().toString()).equals(user.getSenha())){
            JOptionPane.showMessageDialog(rootPane, "Senha anterior incorreta!",
                    "IFCE", JOptionPane.ERROR_MESSAGE);
            tfPassBefore.requestFocus();
            return;
        }
        if (tfPassNew.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe a nova senha!",
                    "IFCE", JOptionPane.ERROR_MESSAGE);
            tfPassNew.requestFocus();
            return;
        }
        if (tfConfirmPassNew.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Confirme a nova senha!",
                    "IFCE", JOptionPane.ERROR_MESSAGE);
            tfConfirmPassNew.requestFocus();
            return;
        }
        if(!tfPassNew.getText().equals(tfConfirmPassNew.getText())){
            JOptionPane.showMessageDialog(rootPane, "Senha n√£o confirmada!",
                    "IFCE", JOptionPane.ERROR_MESSAGE);
            tfConfirmPassNew.requestFocus();
            return;
        }
        
        
        
        user.setSenha(tfPassNew.getText());
        uDAO.update(user);
        JOptionPane.showMessageDialog(rootPane, "Senha alterada com sucesso!", "IFCE", 
                JOptionPane.INFORMATION_MESSAGE);
        setVisible(false);

    }//GEN-LAST:event_btSaveActionPerformed

