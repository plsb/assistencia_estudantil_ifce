    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        if (cbStudent.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Informe o Estudante.");
            return;
        }
        ItensRepublicDAO irDAO = new ItensRepublicDAO();
        Student stSelected = (Student) cbStudent.getSelectedItem();
        
        //verifica se estudante faz parte da republica
        List<ItensRepublic> itensRepublicList = irDAO.checkExists("republic", republic, "student", stSelected);
        if(itensRepublicList!=null){
            if(itensRepublicList.size()>0){
                JOptionPane.showMessageDialog(rootPane, "O estudante ["+stSelected.getName()+"] \njá faz parte dessa república." );
                return ;
            }
        }
        
        //verifica se estudante não está cadastrado em outra republica
        itensRepublicList = irDAO.checkExists("student", stSelected);
        if(itensRepublicList!=null){
            if(itensRepublicList.size()>0){
                JOptionPane.showMessageDialog(rootPane, "O estudante ["+stSelected.getName()
                        +"] \njá faz parte de outra república ["+itensRepublicList.get(0).getRepublic().getDescription()+"]." );
                return ;
            }
        }
        
        
        ItensRepublic iRepublic = new ItensRepublic();
        iRepublic.setRepublic(republic);
        iRepublic.setStudent(stSelected);

        ItensRepublicFrmRegister irfr = new ItensRepublicFrmRegister(iRepublic);
        irfr.setVisible(true);
        
        preencheTabela(republic);
       
    }//GEN-LAST:event_btAddActionPerformed

    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
       if (tbRepublic.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha da tabela!");
        } else {
            ItensRepublicTableModel ptm = (ItensRepublicTableModel) tbRepublic.getModel();
            ItensRepublic select = ptm.getItensRepublic(tbRepublic.getSelectedRow());
            if (JOptionPane.showConfirmDialog(rootPane, "Deseja excluir o estudante [" + select.getStudent().getName() + "] "
                    + "da república ["+select.getRepublic().getDescription()+"]? "
                    + "\nOBS: Será excluída todas as permissão para todas as refeições.",
                    "Excluir Estudante", JOptionPane.YES_NO_OPTION)
                    == JOptionPane.YES_OPTION) {
                
                //exclui permissoes
                StudentAllowMealDayDAO samDAO = new StudentAllowMealDayDAO();
                samDAO.executeSQL("delete from allowstudenmealday where student_id="+select.getStudent().getId());
                
                ItensRepublicDAO rDAO = new ItensRepublicDAO();
                try {
                    rDAO.remove(select);
                } catch (Exception ex) {
                      JOptionPane.showMessageDialog(rootPane, "Item de república não pode ser excluído!");
                }
                preencheTabela(republic);
            }

        }
    }//GEN-LAST:event_btDeleteActionPerformed

