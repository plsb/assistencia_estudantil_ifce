    public void insertStudent() {
        cbStudent.removeAllItems();
        cbStudent.addItem("-");

        StudentDAO mdao = new StudentDAO();
        List<Student> list = mdao.list("campus", UserActive.returnCampus(), "name");
        
        list = new ArrayList(new HashSet(list));
        Collections.sort(list);
        for (int i = 0; i < list.size(); i++) {
            cbStudent.addItem(list.get(i));
        }
    }
    
    public void preencheTabela(Republic republic) {
        /*ItensRepublicTableModel ptm = new ItensRepublicTableModel(republic.getItensRepublic());
        tbRepublic.setModel(ptm);*/
        
        ItensRepublicDAO rDAO = new ItensRepublicDAO();
        if (republic == null) {
            List<ItensRepublic> list = new ArrayList<ItensRepublic>();
            list = new ArrayList(new HashSet(list));
            Collections.sort(list);
            ItensRepublicTableModel ptm = new ItensRepublicTableModel(list);
            tbRepublic.setModel(ptm);
        } else {
            List<ItensRepublic> list = rDAO.checkExists("republic", republic);
            list = new ArrayList(new HashSet(list));
            Collections.sort(list);
            ItensRepublicTableModel ptm = new ItensRepublicTableModel(list);
            tbRepublic.setModel(ptm);
        }

        FormatSizeColJTable.packColumns(tbRepublic, 1);
    }

    
