    private void btSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSaveActionPerformed
        // TODO add your handling code here:

        if (tfDesc.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe a Descrição!");
            tfDesc.requestFocus();
            return;
        }

        if (tfCity.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe a Cidade!");
            tfCity.requestFocus();
            return;
        }

        if (tfNeighbohood.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe o Bairro!");
            tfNeighbohood.requestFocus();
            return;
        }

        if (tfAddress.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe o Endereço!");
            tfAddress.requestFocus();
            return;
        }

        
       

        
        if (!tfValue.getText().equals("")) {
            Double valueRepublic = null;
            try {
                valueRepublic = Double.parseDouble(tfValue.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Formato do Valor errado!.");
            }
            republic.setValueRepublic(valueRepublic);
        }
        RepublicDAO cDAO = new RepublicDAO();
       
        republic.setDescription(tfDesc.getText());
        republic.setAddress(tfAddress.getText());
        republic.setCity(tfCity.getText());
        republic.setNeighborhood(tfNeighbohood.getText());
        republic.setOwnerRepublic(tfOwner.getText());
        
        republic.setCampus(UserActive.returnCampus());
        if (republic.getId() == null) {
            cDAO.add(republic);
        } else {
            cDAO.update(republic);
        }
        setVisible(false);

    }//GEN-LAST:event_btSaveActionPerformed

